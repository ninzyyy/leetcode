import collections


def isValidSudoku(board: list[list[str]]) -> bool:

    # Create dictionaries that initializes key:value pair even if it does not exist
    cols = collections.defaultdict(set)
    rows = collections.defaultdict(set)
    squares = collections.defaultdict(set)

    # Move through coordinates of sudoku board
    for r in range(9):
        for c in range(9):

            # Skip if cell is empty
            if board[r][c] == ".":
                continue

            # Check if number is already in row, column or square, if so return False
            if (
                board[r][c] in rows[r]
                or board[r][c] in cols[c]
                or board[r][c] in squares[(r // 3, c // 3)]
            ):
                return False

            # Add non-duplicated number to each respective dictionary
            cols[c].add(board[r][c])
            rows[r].add(board[r][c])
            squares[(r // 3, c // 3)].add(board[r][c])

    return True


